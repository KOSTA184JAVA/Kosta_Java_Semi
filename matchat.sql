CREATE TABLE RESTAURANT(
    SEQ_RESTAUR_ID NUMBER(3),
    RESTAUR_KIND VARCHAR2(10) NOT NULL,
    RESTAUR_NAME VARCHAR2(60) NOT NULL,
    RESTAUR_ADDRESS VARCHAR2(100) NOT NULL,
    RESTAUR_PHONE VARCHAR2(20) NOT NULL,
    RESTAUR_DELIVER VARCHAR2(1) NOT NULL,
    RESTAUR_POINT NUMBER(1) ,
    
    PRIMARY KEY (SEQ_RESTAUR_ID),
    CONSTRAINT CONS_KIND CHECK (RESTAUR_KIND IN ('한식', '중식', '일식', '양식')),
    CONSTRAINT CONS_DELIVER CHECK (RESTAUR_DELIVER IN('O', 'X')),
    CONSTRAINT CONS_POINT CHECK (RESTAUR_POINT BETWEEN 1 AND 5)
);

CREATE TABLE MENU(
    SEQ_MENU_ID NUMBER(4),
    SEQ_RESTAUR_ID NUMBER(3),
    MENU_NAME VARCHAR2(60) NOT NULL,
    MENU_PRICE INTEGER NOT NULL,
    MENU_DESC VARCHAR2(600),
    MENU_KCAL NUMBER(5),
    MENU_SOURCE VARCHAR2(300)NOT NULL,
    
    PRIMARY KEY (SEQ_MENU_ID),
    FOREIGN KEY (SEQ_RESTAUR_ID) REFERENCES RESTAURANT(SEQ_RESTAUR_ID)
);

CREATE TABLE MEMBER(
    MEMBER_ID VARCHAR2(30) NOT NULL,
    MEMBER_PASSWORD VARCHAR2(30) NOT NULL,
    MEMBER_NAME VARCHAR2(30) NOT NULL,
    MEMBER_PHONE VARCHAR2(20) NOT NULL,
    MEMBER_ADDRESS VARCHAR2(50), 
    PRIMARY KEY (MEMBER_ID)
);

